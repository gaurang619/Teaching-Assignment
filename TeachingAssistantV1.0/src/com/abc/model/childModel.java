package com.abc.model;

public class childModel {
	private int cid;
	private int mid;
	private String hob;
	
	public childModel(int mid, String hob) {
		super();
		this.mid = mid;
		this.hob = hob;
	}
	public int getCid() {
		return cid;
	}
	public int getMid() {
		return mid;
	}
	public String getHob() {
		return hob;
	}
	
	
	
}
